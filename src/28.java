public class MathProblems {
    public static void main(String[] args) {
        // Example of a simple mathematical expression
        System.out.println("The result of 5 + 3 is: " + (5 + 3));
        
        // Example of an arithmetic operation with floating-point numbers
        double num1 = 4.9;
        double num2 = 3.8;
        double result = num1 / num2; // The result of 4.9 / 3.8 is approximately 1.378690563.
        
        System.out.println("The result of " + num1 + " divided by " + num2 + " is: " + result);
    }
}
