public class MathProblems {
    public static void main(String[] args) {
        // Example of simple arithmetic operations
        int result1 = 2 + 3;  // 5
        System.out.println("The result is: " + result1);
        
        double result2 = 4 - 2.5;  // 1.5
        System.out.println("The result is: " + result2);
        
        float result3 = 9 / 3f;  // 3.0
        
        if (result3 > 5) {
            System.out.println("Result of the if statement is greater than 5.");
        } else {
            System.out.println("Result of the if statement is not greater than 5.");
        }
    }
}
